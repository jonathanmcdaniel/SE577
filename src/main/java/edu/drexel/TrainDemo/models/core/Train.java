package edu.drexel.TrainDemo.models.core;

public class Train {

    private String name;
    private int capacity;
    private int coachSeats;
    private int economySeats;

    public String getName(){
        return name;
    }

    public int getCapacity(){
        return capacity;
    }

    public int getCoachSeats(){
        return coachSeats;
    }

    public int getEconomySeats(){
        return economySeats;
    }

}